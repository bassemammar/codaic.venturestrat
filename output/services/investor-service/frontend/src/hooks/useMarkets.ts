export function useMarketsDropdown(activeOnly: boolean = true) {
  const queryResult = useQuery<MarketDropdownOption[], Error>({
    queryKey: marketKeys.dropdown(activeOnly),
    queryFn: () => marketService.getForDropdown(!activeOnly),
    staleTime: MARKET_STALE_TIME,
    gcTime: MARKET_GC_TIME,
    retry: RETRY_COUNT,
    retryDelay,
  });

  // Memoize the options transformation
  const options = useMemo(() => queryResult.data ?? [], [queryResult.data]);

  return {
    ...queryResult,
    options,
  };
}

