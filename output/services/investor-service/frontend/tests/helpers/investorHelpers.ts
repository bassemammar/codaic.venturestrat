/**
 * Submit the Investor form and wait for response
 *
 * @param page - Playwright page object
 * @param expectSuccess - Whether to expect success (default: true)
 * @returns True if form was submitted (success or expected error)
 */
export async function submitInvestorForm(
  page: Page,
  expectSuccess = true
): Promise<boolean> {
  console.log(`[InvestorHelpers] Submitting form, expecting ${expectSuccess ? 'success' : 'error'}`);

  const submitButton = page.locator('[data-testid="submit-button"]');
  await submitButton.click();

  if (expectSuccess) {
    await waitForSuccessNotification(page);
    console.log(`[InvestorHelpers] Form submitted successfully`);
    return true;
  } else {
    // Wait for either error notification or form errors
    await Promise.race([
      waitForErrorNotification(page).catch(() => {}),
      page.locator('[data-testid="form-errors-summary"]').waitFor({ state: 'visible' }).catch(() => {}),
    ]);
    console.log(`[InvestorHelpers] Form submission failed as expected`);
    return true;
  }
}

/**
 * Cancel the Investor form and return to list
 *
 * @param page - Playwright page object
 */
export async function cancelInvestorForm(page: Page): Promise<void> {
  console.log(`[InvestorHelpers] Cancelling form`);

  const cancelButton = page.locator('[data-testid="cancel-button"]');
  await cancelButton.click();
  await waitForInvestorList(page);

  console.log(`[InvestorHelpers] Form cancelled, back to list`);
}

