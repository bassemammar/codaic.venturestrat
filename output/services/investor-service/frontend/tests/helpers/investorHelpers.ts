/**
 * Create a new Investor with provided data
 *
 * Complete workflow: navigate to form, fill data, submit, verify success
 *
 * @param page - Playwright page object
 * @param data - Test data to create with
 * @returns True if creation was successful
 *
 * @example
 * ```typescript
 * const testData = generateInvestorTestData('complete');
 * const created = await createInvestor(page, testData);
 * expect(created).toBeTruthy();
 * ```
 */
export async function createInvestor(
  page: Page,
  data: Partial<InvestorCreateRequest>
): Promise<boolean> {
  console.log(`[InvestorHelpers] Creating new Investor`);

  try {
    // Navigate to create form
    await navigateToCreateInvestor(page);

    // Fill form with data
    await fillInvestorForm(page, data);

    // Submit and wait for success
    await submitInvestorForm(page, true);

    // Wait for list to reload
    await waitForInvestorList(page);

    console.log(`[InvestorHelpers] Investor created successfully`);
    return true;
  } catch (error) {
    console.error(`[InvestorHelpers] Failed to create Investor:`, error);
    return false;
  }
}

/**
 * Update an existing Investor
 *
 * @param page - Playwright page object
 * @param displayValue - Display value to find the entity
 * @param data - Updated data
 * @returns True if update was successful
 */
export async function updateInvestor(
  page: Page,
  displayValue: string,
  data: Partial<InvestorCreateRequest>
): Promise<boolean> {
  console.log(`[InvestorHelpers] Updating Investor: ${displayValue}`);

  try {
    // Find and open edit form
    const opened = await editInvestorByName(page, displayValue);
    if (!opened) {
      return false;
    }

    // Fill form with updated data
    await fillInvestorForm(page, data, { clearFirst: true });

    // Submit and wait for success
    await submitInvestorForm(page, true);

    // Wait for list to reload
    await waitForInvestorList(page);

    console.log(`[InvestorHelpers] Investor updated successfully`);
    return true;
  } catch (error) {
    console.error(`[InvestorHelpers] Failed to update Investor:`, error);
    return false;
  }
}

/**
 * Delete a Investor and verify deletion
 *
 * @param page - Playwright page object
 * @param displayValue - Display value to find the entity
 * @returns True if deletion was successful
 */
export async function deleteInvestor(
  page: Page,
  displayValue: string
): Promise<boolean> {
  console.log(`[InvestorHelpers] Deleting Investor: ${displayValue}`);

  try {
    const deleted = await deleteInvestorByName(page, displayValue);
    if (!deleted) {
      return false;
    }

    // Wait for success notification
    await waitForSuccessNotification(page);

    // Verify entity is removed from list
    await waitForInvestorList(page);
    const stillExists = await investorExistsInList(page, displayValue);

    if (stillExists) {
      console.error(`[InvestorHelpers] Investor still exists after deletion`);
      return false;
    }

    console.log(`[InvestorHelpers] Investor deleted successfully`);
    return true;
  } catch (error) {
    console.error(`[InvestorHelpers] Failed to delete Investor:`, error);
    return false;
  }
}

