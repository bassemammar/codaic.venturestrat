/**
 * Hook for currency-aware amount formatting and validation
 *
 * Provides formatting utilities with proper decimal precision
 * based on ISO 4217 currency standards.
 *
 * @example
 * ```typescript
 * const { formatAmount, getDecimalPrecision, roundToDecimalPrecision } =
 *   useCurrencyFormatting();
 *
 * formatAmount(1234567.89, 'USD'); // "1,234,567.89"
 * formatAmount(1234567, 'JPY');    // "1,234,567"
 * formatAmount(1234.567, 'BHD');   // "1,234.567"
 *
 * getDecimalPrecision('USD'); // 2
 * getDecimalPrecision('JPY'); // 0
 * ```
 */
export function useCurrencyFormatting(): {
  formatAmount: (
    amount: number,
    currencyCode: string,
    options?: CurrencyFormattingOptions
  ) => string;
  formatWithSymbol: (
    amount: number,
    currencyCode: string,
    options?: Omit<CurrencyFormattingOptions, 'includeSymbol'>
  ) => string;
  getDecimalPrecision: (currencyCode: string) => number;
  roundToDecimalPrecision: (amount: number, currencyCode: string) => number;
  getInputStep: (currencyCode: string) => number;
  formatForInput: (amount: number, currencyCode: string) => string;
  parseFormattedAmount: (
    formattedValue: string,
    currencyCode?: string
  ) => { value: number; isValid: boolean; error?: string };
} {
  /**
   * Get decimal precision for a currency
   */
  const getDecimalPrecision = useCallback((currencyCode: string): number => {
    const upperCode = currencyCode.toUpperCase();
    return CURRENCY_DECIMAL_PLACES[upperCode] ?? DEFAULT_DECIMAL_PLACES;
  }, []);

  /**
   * Round amount to currency's decimal precision
   */
  const roundToDecimalPrecision = useCallback(
    (amount: number, currencyCode: string): number => {
      const precision = getDecimalPrecision(currencyCode);
      const factor = Math.pow(10, precision);
      return Math.round(amount * factor) / factor;
    },
    [getDecimalPrecision]
  );

  /**
   * Get step value for input fields
   */
  const getInputStep = useCallback(
    (currencyCode: string): number => {
      const precision = getDecimalPrecision(currencyCode);
      return Math.pow(10, -precision);
    },
    [getDecimalPrecision]
  );

  /**
   * Format amount with currency-specific decimal precision
   */
  const formatAmount = useCallback(
    (
      amount: number,
      currencyCode: string,
      options: CurrencyFormattingOptions = {}
    ): string => {
      const {
        useThousandsSeparator = true,
        includeSymbol = false,
        decimalPrecision,
        locale = 'en-US',
        useAccountingFormat = false,
      } = options;

      const precision = decimalPrecision ?? getDecimalPrecision(currencyCode);
      const isNegative = amount < 0;
      const absoluteAmount = Math.abs(amount);

      const formatter = new Intl.NumberFormat(locale, {
        style: 'decimal',
        minimumFractionDigits: precision,
        maximumFractionDigits: precision,
        useGrouping: useThousandsSeparator,
      });

      let formatted = formatter.format(absoluteAmount);

      if (includeSymbol) {
        // Simple symbol lookup - could be extended
        const symbols: Record<string, string> = {
          USD: '$',
          EUR: '€',
          GBP: '£',
          JPY: '¥',
          CNY: '¥',
        };
        const symbol = symbols[currencyCode.toUpperCase()] || currencyCode;
        formatted = `${symbol}${formatted}`;
      }

      if (isNegative) {
        formatted = useAccountingFormat ? `(${formatted})` : `-${formatted}`;
      }

      return formatted;
    },
    [getDecimalPrecision]
  );

  /**
   * Format amount with currency symbol
   */
  const formatWithSymbol = useCallback(
    (
      amount: number,
      currencyCode: string,
      options: Omit<CurrencyFormattingOptions, 'includeSymbol'> = {}
    ): string => {
      return formatAmount(amount, currencyCode, { ...options, includeSymbol: true });
    },
    [formatAmount]
  );

  /**
   * Format for input field (no separators)
   */
  const formatForInput = useCallback(
    (amount: number, currencyCode: string): string => {
      const precision = getDecimalPrecision(currencyCode);
      const rounded = roundToDecimalPrecision(amount, currencyCode);
      return rounded.toFixed(precision);
    },
    [getDecimalPrecision, roundToDecimalPrecision]
  );

  /**
   * Parse formatted string back to number
   */
  const parseFormattedAmount = useCallback(
    (
      formattedValue: string,
      currencyCode?: string
    ): { value: number; isValid: boolean; error?: string } => {
      if (!formattedValue || formattedValue.trim() === '') {
        return { value: 0, isValid: false, error: 'Empty value' };
      }

      try {
        // Remove currency symbols, thousands separators, whitespace
        let cleanValue = formattedValue
          .replace(/[^0-9.,\-()]/g, '')
          .replace(/,/g, '')
          .trim();

        // Handle accounting format
        const isNegative = cleanValue.startsWith('(') && cleanValue.endsWith(')');
        if (isNegative) {
          cleanValue = cleanValue.slice(1, -1);
        }

        const hasNegativeSign = cleanValue.startsWith('-');
        if (hasNegativeSign) {
          cleanValue = cleanValue.slice(1);
        }

        const parsed = parseFloat(cleanValue);

        if (isNaN(parsed)) {
          return { value: 0, isValid: false, error: 'Invalid number format' };
        }

        const finalValue = isNegative || hasNegativeSign ? -parsed : parsed;

        if (currencyCode) {
          const rounded = roundToDecimalPrecision(finalValue, currencyCode);
          return { value: rounded, isValid: true };
        }

        return { value: finalValue, isValid: true };
      } catch {
        return { value: 0, isValid: false, error: 'Failed to parse amount' };
      }
    },
    [roundToDecimalPrecision]
  );

  return {
    formatAmount,
    formatWithSymbol,
    getDecimalPrecision,
    roundToDecimalPrecision,
    getInputStep,
    formatForInput,
    parseFormattedAmount,
  };
}

