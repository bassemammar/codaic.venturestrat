/**
 * Hook for business day validation with hybrid client/server validation
 *
 * Provides instant client-side validation for weekend/known holidays,
 * with optional server verification for authoritative holiday data.
 *
 * @example
 * ```typescript
 * const { validateDate, verifyWithServer, getNextBusinessDay } =
 *   useBusinessDayValidation({ calendar: 'US' });
 *
 * const result = validateDate(new Date('2024-07-04'));
 * // { isValid: false, message: 'Date is a holiday (Independence Day)', ... }
 * ```
 */
export function useBusinessDayValidation(
  options: UseBusinessDayValidationOptions = {}
): {
  validateDate: (date: Date | string) => BusinessDayValidationResult;
  verifyWithServer: (date: Date | string) => Promise<BusinessDayValidationResult>;
  getNextBusinessDay: (date: Date | string) => Date;
  isWeekend: (date: Date | string) => boolean;
  isKnownHoliday: (date: Date | string) => { isHoliday: boolean; name?: string };
  calendar: Calendar;
  isVerifying: boolean;
  invalidateCache: () => void;
} {
  const {
    calendar = 'WEEKEND_ONLY',
    apiEndpoint = '/api/v1/calendar/validate-business-day',
  } = options;

  const queryClient = useQueryClient();

  /**
   * Parse date input to Date object
   */
  const parseDate = useCallback((date: Date | string): Date => {
    if (date instanceof Date) {
      return date;
    }
    const parsed = new Date(date);
    if (isNaN(parsed.getTime())) {
      throw new Error(`Invalid date: ${date}`);
    }
    return parsed;
  }, []);

  /**
   * Check if a date is a weekend
   */
  const isWeekend = useCallback(
    (date: Date | string): boolean => {
      const d = parseDate(date);
      const day = d.getDay();
      return day === 0 || day === 6;
    },
    [parseDate]
  );

  /**
   * Check if a date is a known holiday (client-side)
   */
  const isKnownHoliday = useCallback(
    (date: Date | string): { isHoliday: boolean; name?: string } => {
      const d = parseDate(date);
      const month = d.getMonth() + 1;
      const day = d.getDate();

      const holidays = COMMON_HOLIDAYS[calendar] || [];
      const matchingHoliday = holidays.find((h) => h.month === month && h.day === day);

      if (matchingHoliday) {
        return { isHoliday: true, name: matchingHoliday.name };
      }

      return { isHoliday: false };
    },
    [calendar, parseDate]
  );

  /**
   * Get the next business day from a given date
   */
  const getNextBusinessDay = useCallback(
    (date: Date | string): Date => {
      let nextDay = parseDate(date);
      nextDay = new Date(nextDay.getTime() + 24 * 60 * 60 * 1000);

      let iterations = 0;
      const maxIterations = 14; // Safety limit (2 weeks)

      while (iterations < maxIterations) {
        if (!isWeekend(nextDay)) {
          const { isHoliday } = isKnownHoliday(nextDay);
          if (!isHoliday) {
            return nextDay;
          }
        }
        nextDay = new Date(nextDay.getTime() + 24 * 60 * 60 * 1000);
        iterations++;
      }

      return nextDay;
    },
    [parseDate, isWeekend, isKnownHoliday]
  );

  /**
   * Format date for display
   */
  const formatDateDisplay = useCallback((date: Date): string => {
    return date.toLocaleDateString('en-US', {
      weekday: 'short',
      year: 'numeric',
      month: 'short',
      day: 'numeric',
    });
  }, []);

  /**
   * Validate a date client-side (instant)
   */
  const validateDate = useCallback(
    (date: Date | string): BusinessDayValidationResult => {
      try {
        const d = parseDate(date);
        const weekend = isWeekend(d);
        const holiday = isKnownHoliday(d);

        if (weekend) {
          const nextBizDay = getNextBusinessDay(d);
          return {
            isValid: false,
            message: `${formatDateDisplay(d)} falls on a weekend`,
            suggestedDate: nextBizDay,
            suggestedDateFormatted: formatDateDisplay(nextBizDay),
            calendar,
            isWeekend: true,
            isHoliday: false,
          };
        }

        if (holiday.isHoliday) {
          const nextBizDay = getNextBusinessDay(d);
          return {
            isValid: false,
            message: `${formatDateDisplay(d)} is a holiday${holiday.name ? ` (${holiday.name})` : ''}`,
            suggestedDate: nextBizDay,
            suggestedDateFormatted: formatDateDisplay(nextBizDay),
            calendar,
            isWeekend: false,
            isHoliday: true,
            holidayName: holiday.name,
          };
        }

        return {
          isValid: true,
          message: 'Date is a valid business day',
          calendar,
          isWeekend: false,
          isHoliday: false,
        };
      } catch (error) {
        return {
          isValid: false,
          message: error instanceof Error ? error.message : 'Invalid date',
          calendar,
        };
      }
    },
    [parseDate, isWeekend, isKnownHoliday, getNextBusinessDay, formatDateDisplay, calendar]
  );

  /**
   * Verify a date with server (lazy query)
   */
  const verifyWithServer = useCallback(
    async (date: Date | string): Promise<BusinessDayValidationResult> => {
      const d = parseDate(date);
      const dateStr = d.toISOString().split('T')[0];

      // Build cache key
      const cacheKey = businessDayKeys.verification(dateStr, calendar);

      try {
        // Check for cached result
        const cachedData = queryClient.getQueryData<BusinessDayValidationResult>(cacheKey);
        const queryState = queryClient.getQueryState(cacheKey);

        const now = Date.now();
        const isCacheFresh =
          queryState?.dataUpdatedAt && now - queryState.dataUpdatedAt < BUSINESS_DAY_STALE_TIME;

        if (cachedData && isCacheFresh) {
          return cachedData;
        }

        // Fetch from server
        const params = new URLSearchParams({
          date: dateStr,
          calendar,
        });

        const response = await fetch(`${apiEndpoint}?${params}`);

        if (!response.ok) {
          throw new Error(`Server verification failed: ${response.status}`);
        }

        const serverData = await response.json();
        const result: BusinessDayValidationResult = {
          isValid: serverData.is_valid,
          message: serverData.message,
          calendar,
          isWeekend: serverData.is_weekend,
          isHoliday: serverData.is_holiday,
          holidayName: serverData.holiday_name,
        };

        // Cache the result
        queryClient.setQueryData(cacheKey, result);

        return result;
      } catch (error) {
        // Fallback to client-side validation
        console.warn('Server verification failed, using client-side result:', error);
        return validateDate(date);
      }
    },
    [parseDate, calendar, queryClient, apiEndpoint, validateDate]
  );

  /**
   * Invalidate cached results
   */
  const invalidateCache = useCallback(() => {
    queryClient.invalidateQueries({ queryKey: businessDayKeys.all });
  }, [queryClient]);

  /**
   * Track verification state
   */
  const isVerifying = queryClient.isFetching({ queryKey: businessDayKeys.all }) > 0;

  return {
    validateDate,
    verifyWithServer,
    getNextBusinessDay,
    isWeekend,
    isKnownHoliday,
    calendar,
    isVerifying,
    invalidateCache,
  };
}

