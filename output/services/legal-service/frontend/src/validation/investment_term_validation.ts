/**
 * Combined validation hook for InvestmentTerm forms
 *
 * Provides all validation functions needed for InvestmentTerm forms,
 * combining business day, crossfield, and currency validation as applicable.
 *
 * @example
 * ```typescript
 * const { validate, validateField, errors, isValid } =
 *   useInvestmentTermValidation({ calendar: 'US' });
 *
 * // Validate entire form
 * const result = validate(formValues);
 * if (!result.isValid) {
 *   console.log('Errors:', result.errors);
 * }
 *
 * // Validate single field
 * const fieldResult = validateField('settlementDate', formValues.settlementDate);
 * ```
 */
export function useInvestmentTermValidation(
options: UseInvestmentTermValidationOptions = {}
): {
  /** Validate entire form */
  validate: (values: InvestmentTermFormValues) => ValidationResult;
  /** Validate single field */
  validateField: (field: keyof InvestmentTermFormValues, value: unknown, allValues?: InvestmentTermFormValues) => ValidationError | null;
  /** Business day validation functions */
  businessDay: ReturnType<typeof useBusinessDayValidation>;
  /** Currency formatting functions */
  currency: ReturnType<typeof useCurrencyFormatting>;
} {
  const businessDay = useBusinessDayValidation({
    calendar: options.calendar,
    currency: options.currency,
  });


  const currency = useCurrencyFormatting();

  /**
   * Validate a single field
   */
  const validateField = useCallback(
    (
      _field: keyof InvestmentTermFormValues,
      _value: unknown,
      _allValues?: InvestmentTermFormValues
    ): ValidationError | null => {
      // Date field validation

      // Amount field validation
      if (_field === 'investment_amount' && _value !== undefined && _value !== null) {
        const currencyCode = _allValues?.currency || 'USD';
        const precision = currency.getDecimalPrecision(currencyCode);
        const numValue = Number(_value);

        if (isNaN(numValue)) {
          return {
            field: 'investment_amount',
            message: 'Invalid amount',
            severity: 'error',
            type: 'amount',
          };
        }

        // Check precision
        const decimalPlaces = (numValue.toString().split('.')[1] || '').length;
        if (decimalPlaces > precision) {
          return {
            field: 'investment_amount',
            message: `Amount precision exceeds ${precision} decimal places for ${currencyCode}`,
            severity: 'warning',
            type: 'precision',
          };
        }
      }

      return null;
    },
    [businessDay, currency]
  );

  /**
   * Validate entire form
   */
  const validate = useCallback(
    (values: InvestmentTermFormValues): ValidationResult => {
      const errors: ValidationError[] = [];

      // Validate each field
      for (const [field, value] of Object.entries(values)) {
        const error = validateField(
          field as keyof InvestmentTermFormValues,
          value,
          values
        );
        if (error) {
          errors.push(error);
        }
      }


      // Remove duplicate errors (same field and message)
      const uniqueErrors = errors.filter(
        (error, index, self) =>
          index === self.findIndex((e) => e.field === error.field && e.message === error.message)
      );

      return {
        isValid: uniqueErrors.filter((e) => e.severity === 'error').length === 0,
        errors: uniqueErrors,
      };
    },
    [validateField]
  );

  return {
    validate,
    validateField,
    businessDay,
    currency,
  };
}

