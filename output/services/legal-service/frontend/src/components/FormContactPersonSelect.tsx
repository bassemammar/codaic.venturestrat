/**
 * FormContactPersonSelect Component
 *
 * A dropdown for selecting contact_persons with React Hook Form integration.
 * Automatically fetches contact_persons using the useContactPersonsDropdown hook
 * and handles loading and error states.
 */
export const FormContactPersonSelect: React.FC<FormContactPersonSelectProps> = memo(
  ({
    name,
    label,
    placeholder = 'Select contact person...',
    required = false,
    disabled = false,
    helperText,
    className,
    style,
    fullWidth = true,
    size = 'medium',
    tabIndex,
    'aria-label': ariaLabel,
    'aria-describedby': ariaDescribedBy,
    'data-testid': dataTestId,
    activeOnly = true,
    onChange,
    clearable = true,
    showExtendedInfo = false,
  }) => {
    const { control } = useFormContext();

    // Fetch contact_persons for dropdown
    const {
      options,
      isLoading,
      isError,
      error,
      refetch,
    } = useContactPersonsDropdown(activeOnly);

    // Memoize options for performance
    const dropdownOptions = useMemo(() => options ?? [], [options]);

    // Find option by value
    const findOptionByValue = (value: string | null | undefined): ContactPersonDropdownOption | null => {
      if (!value) return null;
      return dropdownOptions.find((opt) => opt.value === value) ?? null;
    };

    // Generate IDs for accessibility
    const inputId = `${name}-contact_person-select`;
    const errorId = `${name}-error`;
    const helperId = `${name}-helper`;

    // -------------------------------------------------------------------------
    // Loading State
    // -------------------------------------------------------------------------

    if (isLoading) {
      return (
        <SelectLoadingSkeleton
          label={label}
          required={required}
          fullWidth={fullWidth}
          size={size}
          className={className}
          style={style}
          data-testid={dataTestId}
        />
      );
    }

    // -------------------------------------------------------------------------
    // Error State
    // -------------------------------------------------------------------------

    if (isError && error) {
      return (
        <SelectErrorState
          error={error}
          onRetry={() => refetch()}
          label={label}
          fullWidth={fullWidth}
          className={className}
          style={style}
          data-testid={dataTestId}
        />
      );
    }

    // -------------------------------------------------------------------------
    // Render Select
    // -------------------------------------------------------------------------

    return (
      <Controller
        name={name}
        control={control}
        rules={{
          required: required ? 'Please select a contact person' : false,
        }}
        render={({ field, fieldState: { error: fieldError }}) => {
          const hasError = !!fieldError;
          const errorMessage = fieldError?.message;

          // Find the current option based on field value
          const selectedOption = findOptionByValue(field.value);

          // Handle change
          const handleChange = (
            _event: React.SyntheticEvent,
            newValue: ContactPersonDropdownOption | null
          ) => {
            const newId = newValue?.value ?? null;
            field.onChange(newId);
            if (onChange) {
              onChange(newId, newValue);
            }
          };

          return (
            <Autocomplete<ContactPersonDropdownOption, false, boolean, false>
              id={inputId}
              value={selectedOption}
              onChange={handleChange}
              onBlur={field.onBlur}
              options={dropdownOptions}
              getOptionLabel={(option) => option.label}
              isOptionEqualToValue={(option, value) => option.value === value.value}
              disabled={disabled}
              disableClearable={required || !clearable}
              fullWidth={fullWidth}
              size={size}
              className={className}
              style={style}
              data-testid={dataTestId}
              renderOption={(props, option) => (
                <Box
                  component="li"
                  {...props}
                  key={option.value}
                  sx={{
                    display: 'flex',
                    flexDirection: showExtendedInfo ? 'column' : 'row',
                    alignItems: showExtendedInfo ? 'flex-start' : 'center',
                    gap: 0.5,
                  }}
                >
                  <Typography variant="body2">{option.label}</Typography>
                  {showExtendedInfo && (
                    <Typography variant="caption" color="text.secondary">
                      ID: {option.value}
                    </Typography>
                  )}
                </Box>
              )}
              renderInput={(params) => (
                <TextField
                  {...params}
                  label={label}
                  placeholder={placeholder}
                  required={required}
                  error={hasError}
                  helperText={hasError ? errorMessage : helperText}
                  inputProps={{
                    ...params.inputProps,
                    tabIndex,
                    'aria-label': ariaLabel || label,
                    'aria-describedby': ariaDescribedBy ||
                      (hasError ? errorId : helperText ? helperId : undefined),
                    'aria-invalid': hasError,
                    'aria-required': required,
                  }}
                  FormHelperTextProps={{
                    id: hasError ? errorId : helperId,
                    role: hasError ? 'alert' : undefined,
                  }}
                />
              )}
              noOptionsText="No contact persons found"
              loadingText="Loading contact persons..."
            />
          );
        }}
      />
    );
  },
  // Custom memo comparison
  (prevProps, nextProps) => {
    return (
      prevProps.name === nextProps.name &&
      prevProps.label === nextProps.label &&
      prevProps.placeholder === nextProps.placeholder &&
      prevProps.helperText === nextProps.helperText &&
      prevProps.disabled === nextProps.disabled &&
      prevProps.required === nextProps.required &&
      prevProps.fullWidth === nextProps.fullWidth &&
      prevProps.size === nextProps.size &&
      prevProps.className === nextProps.className &&
      prevProps.activeOnly === nextProps.activeOnly &&
      prevProps.clearable === nextProps.clearable &&
      prevProps.showExtendedInfo === nextProps.showExtendedInfo
    );
  }
);

