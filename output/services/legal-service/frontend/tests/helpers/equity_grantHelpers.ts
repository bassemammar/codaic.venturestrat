/**
 * Delete a EquityGrant and verify deletion
 *
 * @param page - Playwright page object
 * @param displayValue - Display value to find the entity
 * @returns True if deletion was successful
 */
export async function deleteEquityGrant(
  page: Page,
  displayValue: string
): Promise<boolean> {
  console.log(`[EquityGrantHelpers] Deleting EquityGrant: ${displayValue}`);

  try {
    const deleted = await deleteEquityGrantByName(page, displayValue);
    if (!deleted) {
      return false;
    }

    // Wait for success notification
    await waitForSuccessNotification(page);

    // Verify entity is removed from list
    await waitForEquityGrantList(page);
    const stillExists = await equityGrantExistsInList(page, displayValue);

    if (stillExists) {
      console.error(`[EquityGrantHelpers] EquityGrant still exists after deletion`);
      return false;
    }

    console.log(`[EquityGrantHelpers] EquityGrant deleted successfully`);
    return true;
  } catch (error) {
    console.error(`[EquityGrantHelpers] Failed to delete EquityGrant:`, error);
    return false;
  }
}

