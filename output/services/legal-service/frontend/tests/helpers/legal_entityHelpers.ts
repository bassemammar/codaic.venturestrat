/**
 * Delete a LegalEntity and verify deletion
 *
 * @param page - Playwright page object
 * @param displayValue - Display value to find the entity
 * @returns True if deletion was successful
 */
export async function deleteLegalEntity(
  page: Page,
  displayValue: string
): Promise<boolean> {
  console.log(`[LegalEntityHelpers] Deleting LegalEntity: ${displayValue}`);

  try {
    const deleted = await deleteLegalEntityByName(page, displayValue);
    if (!deleted) {
      return false;
    }

    // Wait for success notification
    await waitForSuccessNotification(page);

    // Verify entity is removed from list
    await waitForLegalEntityList(page);
    const stillExists = await legalEntityExistsInList(page, displayValue);

    if (stillExists) {
      console.error(`[LegalEntityHelpers] LegalEntity still exists after deletion`);
      return false;
    }

    console.log(`[LegalEntityHelpers] LegalEntity deleted successfully`);
    return true;
  } catch (error) {
    console.error(`[LegalEntityHelpers] Failed to delete LegalEntity:`, error);
    return false;
  }
}

