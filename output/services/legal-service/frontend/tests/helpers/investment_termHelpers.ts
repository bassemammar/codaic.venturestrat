/**
 * Delete a InvestmentTerm and verify deletion
 *
 * @param page - Playwright page object
 * @param displayValue - Display value to find the entity
 * @returns True if deletion was successful
 */
export async function deleteInvestmentTerm(
  page: Page,
  displayValue: string
): Promise<boolean> {
  console.log(`[InvestmentTermHelpers] Deleting InvestmentTerm: ${displayValue}`);

  try {
    const deleted = await deleteInvestmentTermByName(page, displayValue);
    if (!deleted) {
      return false;
    }

    // Wait for success notification
    await waitForSuccessNotification(page);

    // Verify entity is removed from list
    await waitForInvestmentTermList(page);
    const stillExists = await investmentTermExistsInList(page, displayValue);

    if (stillExists) {
      console.error(`[InvestmentTermHelpers] InvestmentTerm still exists after deletion`);
      return false;
    }

    console.log(`[InvestmentTermHelpers] InvestmentTerm deleted successfully`);
    return true;
  } catch (error) {
    console.error(`[InvestmentTermHelpers] Failed to delete InvestmentTerm:`, error);
    return false;
  }
}

