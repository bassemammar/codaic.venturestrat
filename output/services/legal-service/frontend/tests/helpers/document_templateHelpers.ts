/**
 * Delete a DocumentTemplate and verify deletion
 *
 * @param page - Playwright page object
 * @param displayValue - Display value to find the entity
 * @returns True if deletion was successful
 */
export async function deleteDocumentTemplate(
  page: Page,
  displayValue: string
): Promise<boolean> {
  console.log(`[DocumentTemplateHelpers] Deleting DocumentTemplate: ${displayValue}`);

  try {
    const deleted = await deleteDocumentTemplateByName(page, displayValue);
    if (!deleted) {
      return false;
    }

    // Wait for success notification
    await waitForSuccessNotification(page);

    // Verify entity is removed from list
    await waitForDocumentTemplateList(page);
    const stillExists = await documentTemplateExistsInList(page, displayValue);

    if (stillExists) {
      console.error(`[DocumentTemplateHelpers] DocumentTemplate still exists after deletion`);
      return false;
    }

    console.log(`[DocumentTemplateHelpers] DocumentTemplate deleted successfully`);
    return true;
  } catch (error) {
    console.error(`[DocumentTemplateHelpers] Failed to delete DocumentTemplate:`, error);
    return false;
  }
}

