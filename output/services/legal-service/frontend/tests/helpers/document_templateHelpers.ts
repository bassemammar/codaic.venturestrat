      const cell = row.locator('td').nth(column);
      const cellText = await cell.textContent();
      if (cellText && cellText.includes(displayValue)) {
        console.log(`[DocumentTemplateHelpers] Found DocumentTemplate at row ${i}, column ${column}`);
        return row;
      }
