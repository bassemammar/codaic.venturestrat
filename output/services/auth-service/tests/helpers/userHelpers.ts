/**
 * Update an existing User
 *
 * @param page - Playwright page object
 * @param displayValue - Display value to find the entity
 * @param data - Updated data
 * @returns True if update was successful
 */
export async function updateUser(
  page: Page,
  displayValue: string,
  data: Partial<UserCreateRequest>
): Promise<boolean> {
  console.log(`[UserHelpers] Updating User: ${displayValue}`);

  try {
    // Find and open edit form
    const opened = await editUserByName(page, displayValue);
    if (!opened) {
      return false;
    }

    // Fill form with updated data
    await fillUserForm(page, data, { clearFirst: true });

    // Submit and wait for success
    await submitUserForm(page, true);

    // Wait for list to reload
    await waitForUserList(page);

    console.log(`[UserHelpers] User updated successfully`);
    return true;
  } catch (error) {
    console.error(`[UserHelpers] Failed to update User:`, error);
    return false;
  }
}

/**
 * Delete a User and verify deletion
 *
 * @param page - Playwright page object
 * @param displayValue - Display value to find the entity
 * @returns True if deletion was successful
 */
export async function deleteUser(
  page: Page,
  displayValue: string
): Promise<boolean> {
  console.log(`[UserHelpers] Deleting User: ${displayValue}`);

  try {
    const deleted = await deleteUserByName(page, displayValue);
    if (!deleted) {
      return false;
    }

    // Wait for success notification
    await waitForSuccessNotification(page);

    // Verify entity is removed from list
    await waitForUserList(page);
    const stillExists = await userExistsInList(page, displayValue);

    if (stillExists) {
      console.error(`[UserHelpers] User still exists after deletion`);
      return false;
    }

    console.log(`[UserHelpers] User deleted successfully`);
    return true;
  } catch (error) {
    console.error(`[UserHelpers] Failed to delete User:`, error);
    return false;
  }
}

