/**
 * Hook to fetch a single permission by ID
 *
 * @param id - Permission ID (UUID)
 * @param options - Optional React Query options
 * @returns Query result with permission data
 *
 * @example
 * ```typescript
 * const { data: permission, isLoading } = usePermission('uuid-here');
 * ```
 */
export function usePermission(
  id: string,
  options?: Omit<UseQueryOptions<Permission | null, Error>, 'queryKey' | 'queryFn'>
): UseQueryResult<Permission | null, Error> {
  return useQuery<Permission | null, Error>({
    queryKey: permissionKeys.detail(id),
    queryFn: () => permissionService.getById(id),
    staleTime: PERMISSION_STALE_TIME,
    gcTime: PERMISSION_GC_TIME,
    enabled: !!id,
    retry: RETRY_COUNT,
    retryDelay,
    ...options,
  });
}

