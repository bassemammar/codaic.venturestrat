"""
Tenant context middleware for outreach-service.

Extracts X-Tenant-ID header and attaches to request state for multi-tenant operations.
Generated by codaic CLI.
"""
from typing import Callable
import structlog
from fastapi import Request, Response
from starlette.middleware.base import BaseHTTPMiddleware
from venturestrat.tenancy import set_current_tenant, clear_current_tenant, TenantContext

logger = structlog.get_logger(__name__)


class TenantContextMiddleware(BaseHTTPMiddleware):
    """
    Extract tenant ID from request headers and attach to request state.

    The X-Tenant-ID header is extracted and made available to all endpoints
    via request.state.tenant_id. This enables multi-tenant data isolation
    when using BaseModel ORM.
    """

    async def dispatch(self, request: Request, call_next: Callable) -> Response:
        """
        Process request and extract tenant context.

        Args:
            request: The incoming request
            call_next: The next middleware/endpoint in the chain

        Returns:
            Response from the endpoint
        """
        # Extract tenant ID from header
        tenant_id = request.headers.get("X-Tenant-ID")

        # Attach to request state (available in all endpoints)
        request.state.tenant_id = tenant_id

        # Set tenant context for BaseModel (using contextvars)
        if tenant_id:
            set_current_tenant(TenantContext(tenant_id=tenant_id))
            logger.info("tenant_context_attached", tenant_id=tenant_id)

        try:
            response = await call_next(request)
            return response
        finally:
            clear_current_tenant()
