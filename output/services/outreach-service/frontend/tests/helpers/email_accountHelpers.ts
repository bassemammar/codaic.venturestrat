/**
 * Delete a EmailAccount and verify deletion
 *
 * @param page - Playwright page object
 * @param displayValue - Display value to find the entity
 * @returns True if deletion was successful
 */
export async function deleteEmailAccount(
  page: Page,
  displayValue: string
): Promise<boolean> {
  console.log(`[EmailAccountHelpers] Deleting EmailAccount: ${displayValue}`);

  try {
    const deleted = await deleteEmailAccountByName(page, displayValue);
    if (!deleted) {
      return false;
    }

    // Wait for success notification
    await waitForSuccessNotification(page);

    // Verify entity is removed from list
    await waitForEmailAccountList(page);
    const stillExists = await emailAccountExistsInList(page, displayValue);

    if (stillExists) {
      console.error(`[EmailAccountHelpers] EmailAccount still exists after deletion`);
      return false;
    }

    console.log(`[EmailAccountHelpers] EmailAccount deleted successfully`);
    return true;
  } catch (error) {
    console.error(`[EmailAccountHelpers] Failed to delete EmailAccount:`, error);
    return false;
  }
}

